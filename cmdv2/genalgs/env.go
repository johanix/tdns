package main

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"sort"
	"strings"
)

// libEnv holds the environment a library's -env.sh script exports on a
// successful detection (the `export NAME="value"` lines from stdout).
type libEnv struct {
	group string
	vars  map[string]string
}

// envScriptRel maps a registry Group value to the -env.sh path relative
// to the dnssec-algorithms repo root.
var envScriptRel = map[string]string{
	"liboqs":  "liboqs/liboqs-env.sh",
	"sqisign": "sqisignc/sqisign-env.sh",
	"qruov":   "qruovc/qruov-env.sh",
}

// detectLib runs the group's -env.sh script (via bash) from algrepo. On
// success (exit 0, non-empty stdout) it parses the exported variables
// and returns them. A non-zero exit or missing script is an error — the
// caller turns that into a hard "library not available" failure.
func detectLib(algrepo, group string) (libEnv, error) {
	rel, ok := envScriptRel[group]
	if !ok {
		return libEnv{}, fmt.Errorf("no -env.sh known for group %q", group)
	}
	script := filepath.Join(algrepo, rel)
	if _, err := os.Stat(script); err != nil {
		return libEnv{}, fmt.Errorf("detection script %s not found", script)
	}

	// The scripts print `export NAME="value"` to stdout on success,
	// diagnostics to stderr, and exit non-zero when the lib is absent.
	// They use ${BASH_SOURCE[0]} so must run under bash, not sh.
	cmd := exec.Command("bash", script)
	cmd.Dir = algrepo
	var stdout, stderr strings.Builder
	cmd.Stdout = &stdout
	cmd.Stderr = &stderr
	err := cmd.Run()
	if err != nil {
		msg := strings.TrimSpace(stderr.String())
		if msg == "" {
			msg = err.Error()
		}
		return libEnv{}, fmt.Errorf("%s", firstLine(msg))
	}
	vars := parseExports(stdout.String())
	if len(vars) == 0 {
		// Exit 0 but nothing exported — treat as not-detected to be safe.
		return libEnv{}, fmt.Errorf("detection produced no environment (%s)", firstLine(stderr.String()))
	}
	return libEnv{group: group, vars: vars}, nil
}

// parseExports pulls `export NAME="value"` (or NAME=value) lines out of
// the script's stdout.
func parseExports(out string) map[string]string {
	vars := map[string]string{}
	for _, line := range strings.Split(out, "\n") {
		line = strings.TrimSpace(line)
		line = strings.TrimPrefix(line, "export ")
		eq := strings.IndexByte(line, '=')
		if eq <= 0 {
			continue
		}
		name := strings.TrimSpace(line[:eq])
		val := strings.TrimSpace(line[eq+1:])
		val = strings.Trim(val, `"'`)
		if name == "" || val == "" {
			continue
		}
		vars[name] = val
	}
	return vars
}

func firstLine(s string) string {
	if i := strings.IndexByte(s, '\n'); i >= 0 {
		return s[:i]
	}
	return s
}

// genEnvMk builds the Makefile fragment the app includes. It merges the
// per-library environments: PKG_CONFIG_PATH entries are joined, other
// variables (CGO_LDFLAGS, LD_LIBRARY_PATH) are collected. Variables are
// exported so they reach the `go build` subprocess.
func genEnvMk(needGroups map[string]bool, envByGroup map[string]libEnv) []byte {
	var b strings.Builder
	b.WriteString("# Code generated by genalgs; DO NOT EDIT.\n")
	b.WriteString("# Build environment for the C-backed algorithms this app links.\n")
	b.WriteString("# Included by the app Makefile so cgo/pkg-config can find the libraries.\n\n")

	if len(needGroups) == 0 {
		b.WriteString("# (no C-backed algorithms selected; nothing to set)\n")
		return []byte(b.String())
	}

	// Collect, joining PKG_CONFIG_PATH across libraries and unioning the rest.
	pkgPaths := []string{}
	others := map[string][]string{}
	for _, g := range sortedGroups(needGroups) {
		env := envByGroup[g]
		names := make([]string, 0, len(env.vars))
		for n := range env.vars {
			names = append(names, n)
		}
		sort.Strings(names)
		for _, n := range names {
			v := env.vars[n]
			if n == "PKG_CONFIG_PATH" {
				pkgPaths = append(pkgPaths, v)
			} else {
				others[n] = appendUnique(others[n], v)
			}
		}
	}

	if len(pkgPaths) > 0 {
		fmt.Fprintf(&b, "export PKG_CONFIG_PATH := %s$(if $(PKG_CONFIG_PATH),:$(PKG_CONFIG_PATH))\n",
			strings.Join(pkgPaths, ":"))
	}
	otherNames := make([]string, 0, len(others))
	for n := range others {
		otherNames = append(otherNames, n)
	}
	sort.Strings(otherNames)
	for _, n := range otherNames {
		fmt.Fprintf(&b, "export %s := %s\n", n, strings.Join(others[n], " "))
	}
	return []byte(b.String())
}

func appendUnique(xs []string, v string) []string {
	for _, x := range xs {
		if x == v {
			return xs
		}
	}
	return append(xs, v)
}
